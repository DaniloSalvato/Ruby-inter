module CurrencyConverter

    def real_to_euro(real)
        puts valor_em_euro = real * 0.19
    end

    def real_to_dollar(real)
        puts valor_em_dolar = real * 0.21
    end

    def euro_to_real(euro)
       puts valor_em_real = euro * 5.34
    end

    def euro_to_dollar(euro)
       puts valor_em_dolar = euro * 1.11
    end

    def dollar_to_real(dollar)
       puts valor_em_real = dollar * 4.80
    end
    
    def dollar_to_euro(dollar)
       puts valor_em_euro = dollar * 0.90
    end
end
